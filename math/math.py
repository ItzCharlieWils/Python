import math

x = 9.8
print(f"original value of x = {x}")
result = math.sqrt(x)
print(f"math.sqrt(x) result = {result}")
result = math.ceil(x)
print(f"math.ceil(x) result = {result}")
result = math.floor(x)
print(f"math.floor(x) result = {result}")
result = math.sin(x)
print(f"math.sin(x) result = {result}")
result = math.cos(x)
print(f"math.cos(x) result = {result}")
result = math.tan(x)
print(f"math.tan(x) result = {result}")
print(f"math.pi result = {math.pi}")
